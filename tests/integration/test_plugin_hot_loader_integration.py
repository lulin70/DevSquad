class T1_PluginDiscoveryIntegration(unittest.TestCase):
    """T1: PluginHotLoader discovery wired through the dispatcher."""

    def setUp(self) -> None:
        self._work_dir = tempfile.mkdtemp(prefix="plugin_disc_")
        self._dropin = Path(self._work_dir) / "dropin"
        self._dropin.mkdir()
        self.disp = MultiAgentDispatcher(
            enable_warmup=False,
            enable_memory=False,
            enable_skillify=False,
            persist_dir=self._work_dir,
            plugins_enabled=True,
            plugins_dropin_dir=str(self._dropin),
        )

    def tearDown(self) -> None:
        self.disp.shutdown()
        shutil.rmtree(self._work_dir, ignore_errors=True)

    def test_01_dispatcher_scan_loads_dropin_py_file(self) -> None:
        """Verify: dispatcher.scan_plugins() loads a real .py drop-in via importlib."""
        _write_dropin(self._dropin / "alpha.py", "alpha-tag")
        entries = self.disp.scan_plugins()
        self.assertEqual(len(entries), 1)
        self.assertIsInstance(entries[0], PluginEntry)
        self.assertEqual(entries[0].name, "alpha")
        # Loaded instance is reachable through the dispatcher
        plugin = self.disp.get_plugin("alpha")
        self.assertIsNotNone(plugin)
        self.assertEqual(plugin.name(), "alpha-tag")

    def test_02_default_dropin_dir_created_under_persist_dir(self) -> None:
        """Verify: plugins_dropin_dir=None → factory creates persist_dir/plugins_extra."""
        work_dir = tempfile.mkdtemp(prefix="plugin_default_")
        try:
            disp = MultiAgentDispatcher(
                enable_warmup=False,
                enable_memory=False,
                enable_skillify=False,
                persist_dir=work_dir,
                plugins_enabled=True,
            )
            self.assertIsInstance(disp.plugin_hot_loader, PluginHotLoader)
            expected = (Path(work_dir) / "plugins_extra").resolve()
            self.assertEqual(disp.plugin_hot_loader.dropin_dir, expected)
            self.assertTrue(expected.exists(), "Default drop-in dir must be created by factory")
            disp.shutdown()
        finally:
            shutil.rmtree(work_dir, ignore_errors=True)

    def test_03_custom_dropin_dir_propagated_to_loader(self) -> None:
        """Verify: plugins_dropin_dir kwarg reaches PluginHotLoader.dropin_dir."""
        self.assertEqual(
            self.disp.plugin_hot_loader.dropin_dir,
            self._dropin.resolve(),
        )

    def test_04_scan_skips_init_py_and_non_py_files(self) -> None:
        """Verify: scan ignores __init__.py and non-.py files, loads real plugins only."""
        _write_dropin(self._dropin / "real.py", "real")
        (self._dropin / "__init__.py").write_text("", encoding="utf-8")
        (self._dropin / "notes.txt").write_text("not a plugin", encoding="utf-8")
        entries = self.disp.scan_plugins()
        self.assertEqual(len(entries), 1)
        self.assertEqual(entries[0].name, "real")


