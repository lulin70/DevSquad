class T2_PluginRegistrationIntegration(unittest.TestCase):
    """T2: register_plugin / unregister_plugin / register_builtin_plugin via dispatcher."""

    def setUp(self) -> None:
        self._work_dir = tempfile.mkdtemp(prefix="plugin_reg_")
        self._dropin = Path(self._work_dir) / "dropin"
        self._dropin.mkdir()
        self.disp = MultiAgentDispatcher(
            enable_warmup=False,
            enable_memory=False,
            enable_skillify=False,
            persist_dir=self._work_dir,
            plugins_enabled=True,
            plugins_dropin_dir=str(self._dropin),
        )

    def tearDown(self) -> None:
        self.disp.shutdown()
        shutil.rmtree(self._work_dir, ignore_errors=True)

    def test_01_register_plugin_returns_true_and_retrievable(self) -> None:
        """Verify: register_plugin() returns True and get_plugin() returns same instance."""
        plugin = _StubPlugin("reg")
        self.assertTrue(self.disp.register_plugin("reg", plugin))
        self.assertIs(self.disp.get_plugin("reg"), plugin)

    def test_02_registered_plugin_invokable_via_dispatcher(self) -> None:
        """Verify: plugin retrieved via dispatcher is callable (method returns label)."""
        self.disp.register_plugin("p", _StubPlugin("hello"))
        plugin = self.disp.get_plugin("p")
        self.assertIsNotNone(plugin)
        self.assertEqual(plugin.name(), "hello")

    def test_03_register_duplicate_returns_false(self) -> None:
        """Verify: registering the same name twice returns False (no overwrite)."""
        self.assertTrue(self.disp.register_plugin("dup", _StubPlugin("v1")))
        self.assertFalse(self.disp.register_plugin("dup", _StubPlugin("v2")))
        # Original instance preserved
        self.assertEqual(self.disp.get_plugin("dup").name(), "v1")

    def test_04_unregister_plugin_removes_it(self) -> None:
        """Verify: unregister_plugin() removes the plugin from the loader."""
        self.disp.register_plugin("rm", _StubPlugin())
        self.assertTrue(self.disp.unregister_plugin("rm"))
        self.assertIsNone(self.disp.get_plugin("rm"))
        self.assertNotIn("rm", self.disp.list_plugins())

    def test_05_builtin_and_hot_registered_coexist(self) -> None:
        """Verify: register_builtin_plugin and register_plugin populate the same registry."""
        self.assertTrue(self.disp.register_builtin_plugin("builtin", _StubPlugin("b")))
        self.assertTrue(self.disp.register_plugin("hot", _StubPlugin("h")))
        self.assertEqual(set(self.disp.list_plugins()), {"builtin", "hot"})
        # Sources differ between the two registration paths
        builtin_entry = self.disp.plugin_hot_loader.get_entry("builtin")
        hot_entry = self.disp.plugin_hot_loader.get_entry("hot")
        self.assertEqual(builtin_entry.source, "builtin")
        self.assertEqual(hot_entry.source, "hot_register")


