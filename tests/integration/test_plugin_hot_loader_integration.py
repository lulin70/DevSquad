#!/usr/bin/env python3
"""PluginHotLoader + Dispatcher Integration Tests (V4.2.1 P2-4 — Test Pyramid Improvement).

End-to-end integration tests for PluginHotLoader + MultiAgentDispatcher
cross-module interaction. Verifies that the plugin hot-loading subsystem
wires correctly into the dispatcher across the full lifecycle:

    MultiAgentDispatcher(plugins_enabled=True, plugins_dropin_dir=...)
      → ComponentFactory creates PluginHotLoader
        → scan_plugins() → scan_dropin_dir() → importlib load (create_plugin)
        → reload_plugins() → reload_if_changed() (mtime + checksum, rollback)
      → register_plugin / unregister_plugin / register_builtin_plugin
      → get_plugin / list_plugins

These tests focus on CROSS-MODULE interactions (dispatcher ↔ loader boundary,
config propagation, real .py file drop-in loading, reload rollback). Unit-level
behavior of PluginHotLoader in isolation is covered by
tests/test_plugins_hot_loader.py.

Test categories:
    T1: PluginHotLoader discovery wired through dispatcher
    T2: Plugin registration with dispatcher (hot_register / builtin)
    T3: Hot-reload integration (modify .py → reload → new instance)
    T4: plugins_no_hot_reload mode integration
    T5: Disabled mode integration (plugins_enabled=False → loader None)
    T6: Multiple plugins integration (multi-file scan, coexistence)
    T7: Edge cases + graceful degradation (empty, malformed, raising, rollback)
"""

