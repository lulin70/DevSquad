class T3_HotReloadIntegration(unittest.TestCase):
    """T3: Hot-reload cycle through dispatcher (modify .py → reload → new instance)."""

    def setUp(self) -> None:
        self._work_dir = tempfile.mkdtemp(prefix="plugin_reload_")
        self._dropin = Path(self._work_dir) / "dropin"
        self._dropin.mkdir()
        self.disp = MultiAgentDispatcher(
            enable_warmup=False,
            enable_memory=False,
            enable_skillify=False,
            persist_dir=self._work_dir,
            plugins_enabled=True,
            plugins_dropin_dir=str(self._dropin),
        )

    def tearDown(self) -> None:
        self.disp.shutdown()
        shutil.rmtree(self._work_dir, ignore_errors=True)

    def test_01_reload_no_changes_returns_empty(self) -> None:
        """Verify: reload_plugins() with unchanged files returns an empty list."""
        _write_dropin(self._dropin / "p.py", "v1")
        self.disp.scan_plugins()
        self.assertEqual(self.disp.reload_plugins(), [])

    def test_02_modify_content_triggers_reload(self) -> None:
        """Verify: modifying plugin content → reload_plugins() reports it and updates behavior."""
        fpath = self._dropin / "p.py"
        _write_dropin(fpath, "v1")
        self.disp.scan_plugins()
        self.assertEqual(self.disp.get_plugin("p").name(), "v1")

        time.sleep(0.05)
        _write_dropin(fpath, "v2")
        _bump_mtime(fpath)

        reloaded = self.disp.reload_plugins()
        self.assertEqual(reloaded, ["p"])
        self.assertEqual(self.disp.get_plugin("p").name(), "v2")

    def test_03_reloaded_instance_is_a_new_object(self) -> None:
        """Verify: reload swaps the plugin instance (new object, not mutated in place)."""
        fpath = self._dropin / "p.py"
        _write_dropin(fpath, "v1")
        self.disp.scan_plugins()
        old_instance = self.disp.get_plugin("p")

        time.sleep(0.05)
        _write_dropin(fpath, "v2")
        _bump_mtime(fpath)
        self.disp.reload_plugins()

        new_instance = self.disp.get_plugin("p")
        self.assertIsNot(old_instance, new_instance)

    def test_04_reload_only_affects_changed_plugins(self) -> None:
        """Verify: reload_plugins() returns only the plugins whose content changed."""
        a = self._dropin / "a.py"
        b = self._dropin / "b.py"
        _write_dropin(a, "a1")
        _write_dropin(b, "b1")
        self.disp.scan_plugins()

        time.sleep(0.05)
        _write_dropin(b, "b2")
        _bump_mtime(b)

        reloaded = self.disp.reload_plugins()
        self.assertEqual(reloaded, ["b"])
        self.assertEqual(self.disp.get_plugin("a").name(), "a1")  # unchanged
        self.assertEqual(self.disp.get_plugin("b").name(), "b2")  # reloaded


