class T7_EdgeCasesAndGracefulDegradationIntegration(unittest.TestCase):
    """T7: Empty dir, malformed plugin, raising plugin, reload rollback, audit log."""

    def setUp(self) -> None:
        self._work_dir = tempfile.mkdtemp(prefix="plugin_edge_")
        self._dropin = Path(self._work_dir) / "dropin"
        self._dropin.mkdir()
        self.disp = MultiAgentDispatcher(
            enable_warmup=False,
            enable_memory=False,
            enable_skillify=False,
            persist_dir=self._work_dir,
            plugins_enabled=True,
            plugins_dropin_dir=str(self._dropin),
        )

    def tearDown(self) -> None:
        self.disp.shutdown()
        shutil.rmtree(self._work_dir, ignore_errors=True)

    def test_01_empty_dropin_dir_scan_returns_empty(self) -> None:
        """Verify: scan_plugins() on an empty drop-in dir returns an empty list."""
        self.assertEqual(self.disp.scan_plugins(), [])
        self.assertEqual(self.disp.list_plugins(), [])

    def test_02_malformed_plugin_no_create_plugin_skipped(self) -> None:
        """Verify: a drop-in missing create_plugin() is skipped (graceful, no crash)."""
        _write_broken_dropin(self._dropin / "broken.py")
        entries = self.disp.scan_plugins()
        self.assertEqual(entries, [])
        self.assertIsNone(self.disp.get_plugin("broken"))

    def test_03_create_plugin_raising_runtime_error_returns_none(self) -> None:
        """Verify: create_plugin() raising RuntimeError is swallowed; plugin not registered."""
        _write_raising_dropin(self._dropin / "raising.py")
        entries = self.disp.scan_plugins()
        self.assertEqual(entries, [])
        self.assertIsNone(self.disp.get_plugin("raising"))

    def test_04_reload_rollback_keeps_old_instance(self) -> None:
        """Verify: reload of a broken update keeps the previous working instance."""
        fpath = self._dropin / "p.py"
        _write_dropin(fpath, "v1")
        self.disp.scan_plugins()
        old_instance = self.disp.get_plugin("p")
        self.assertEqual(old_instance.name(), "v1")

        time.sleep(0.05)
        _write_broken_dropin(fpath)
        _bump_mtime(fpath)

        reloaded = self.disp.reload_plugins()
        self.assertNotIn("p", reloaded)  # reload failed
        # Old instance preserved (rollback)
        self.assertIs(self.disp.get_plugin("p"), old_instance)
        self.assertEqual(self.disp.get_plugin("p").name(), "v1")

    def test_05_audit_log_records_dispatcher_reload(self) -> None:
        """Verify: loader audit log records a successful reload triggered via dispatcher."""
        fpath = self._dropin / "p.py"
        _write_dropin(fpath, "v1")
        self.disp.scan_plugins()
        self.disp.plugin_hot_loader.clear_audit_log()

        time.sleep(0.05)
        _write_dropin(fpath, "v2")
        _bump_mtime(fpath)
        self.disp.reload_plugins()

        audits = self.disp.plugin_hot_loader.get_audit_log()
        reload_audits = [a for a in audits if a["method"] == "reload"]
        self.assertTrue(
            any(a["success"] for a in reload_audits),
            f"Expected successful reload audit entry, got: {reload_audits}",
        )


