# ---------------------------------------------------------------------------
# Drop-in plugin file templates + helpers
# ---------------------------------------------------------------------------

_DROPIN_TEMPLATE = '''"""Drop-in plugin generated by integration test."""
from __future__ import annotations


class _Plugin:
    def __init__(self) -> None:
        self.tag = {tag!r}

    def name(self) -> str:
        return self.tag


def create_plugin() -> _Plugin:
    return _Plugin()
'''

_BROKEN_DROPIN_TEMPLATE = '''"""Broken drop-in plugin (no create_plugin)."""
# Intentionally missing create_plugin().
'''

_RAISING_DROPIN_TEMPLATE = '''"""Drop-in plugin whose create_plugin raises RuntimeError."""
from __future__ import annotations


def create_plugin():
    raise RuntimeError("plugin init failed")
'''


def _write_dropin(filepath: Path, tag: str) -> None:
    """Write a valid drop-in plugin file exposing create_plugin()."""
    filepath.write_text(_DROPIN_TEMPLATE.format(tag=tag), encoding="utf-8")


def _write_broken_dropin(filepath: Path) -> None:
    """Write a malformed drop-in plugin (no create_plugin function)."""
    filepath.write_text(_BROKEN_DROPIN_TEMPLATE, encoding="utf-8")


def _write_raising_dropin(filepath: Path) -> None:
    """Write a drop-in plugin whose create_plugin() raises RuntimeError."""
    filepath.write_text(_RAISING_DROPIN_TEMPLATE, encoding="utf-8")


def _bump_mtime(filepath: Path) -> None:
    """Force a filesystem mtime change (robust across low-precision FSes)."""
    future = time.time() + 5
    os.utime(filepath, (future, future))


class _StubPlugin:
    """Minimal plugin instance used for register_plugin / register_builtin tests."""

    def __init__(self, label: str = "stub") -> None:
        self.label = label

    def name(self) -> str:
        return self.label


