class TestHotLoaderConcurrent:
    """场景: 并发安全（threading.RLock）。"""

    def test_concurrent_register_distinct_names(self, tmp_path: Path) -> None:
        loader = PluginHotLoader(dropin_dir=tmp_path)
        n_threads = 10
        n_per_thread = 20
        results: list[bool] = []
        results_lock = threading.Lock()

        def worker(tid: int) -> None:
            local_results: list[bool] = []
            for i in range(n_per_thread):
                name = f"t{tid}-p{i}"
                local_results.append(loader.hot_register(name, StubPlugin(name)))
            with results_lock:
                results.extend(local_results)

        threads = [threading.Thread(target=worker, args=(t,)) for t in range(n_threads)]
        for t in threads:
            t.start()
        for t in threads:
            t.join()

        # 所有注册都应成功（名字互不冲突）
        assert all(results)
        assert len(loader.list_plugins()) == n_threads * n_per_thread

    def test_concurrent_register_same_name_only_one_wins(self, tmp_path: Path) -> None:
        loader = PluginHotLoader(dropin_dir=tmp_path)
        n_threads = 20
        winners: list[bool] = []
        winners_lock = threading.Lock()

        def worker() -> None:
            ok = loader.hot_register("contended", StubPlugin())
            with winners_lock:
                winners.append(ok)

        threads = [threading.Thread(target=worker) for _ in range(n_threads)]
        for t in threads:
            t.start()
        for t in threads:
            t.join()

        # 只有一个线程成功
        assert sum(1 for w in winners if w) == 1
        assert loader.get_plugin("contended") is not None

    def test_concurrent_scan_and_register(self, tmp_path: Path) -> None:
        # 在 dropin 目录写入文件
        for i in range(5):
            _write_dropin(tmp_path / f"f{i}.py", f"tag-{i}")

        loader = PluginHotLoader(dropin_dir=tmp_path)
        errors: list[Exception] = []
        errors_lock = threading.Lock()

        def scan_worker() -> None:
            try:
                loader.scan_dropin_dir()
            except Exception as e:  # noqa: BLE001
                with errors_lock:
                    errors.append(e)

        def register_worker() -> None:
            try:
                for i in range(10):
                    loader.hot_register(f"hot-{i}", StubPlugin())
            except Exception as e:  # noqa: BLE001
                with errors_lock:
                    errors.append(e)

        threads = [
            threading.Thread(target=scan_worker),
            threading.Thread(target=scan_worker),
            threading.Thread(target=register_worker),
            threading.Thread(target=register_worker),
        ]
        for t in threads:
            t.start()
        for t in threads:
            t.join()

        assert errors == []
        # 至少加载了一些 drop-in
        dropin_loaded = [n for n in loader.list_plugins() if n.startswith("f")]
