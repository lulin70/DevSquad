class TestPathTraversalProtection:
    """场景: 路径穿越防护。"""

    def test_path_traversal_invalid_suffix_blocked(self, tmp_path: Path) -> None:
        # 非 .py 后缀的文件不会被加载
        (tmp_path / "evil.txt").write_text("print('evil')", encoding="utf-8")
        (tmp_path / "evil.pyc").write_text("binary", encoding="utf-8")
        loader = PluginHotLoader(dropin_dir=tmp_path)
        entries = loader.scan_dropin_dir()
        assert entries == []

    def test_path_traversal_symlink_outside_blocked(self, tmp_path: Path) -> None:
        # 创建 dropin 目录外的真实文件
        outside = tmp_path.parent / "outside_target.py"
        try:
            _write_dropin(outside, "outside")
            # 在 dropin 目录内创建符号链接
            link_path = tmp_path / "link.py"
            link_path.symlink_to(outside)

            loader = PluginHotLoader(dropin_dir=tmp_path)
            entries = loader.scan_dropin_dir()
            # 符号链接 resolve() 后指向 dropin 目录外，应被拒绝
            assert entries == []
        finally:
            if outside.exists():
                outside.unlink()

    def test_path_traversal_file_size_limit(self, tmp_path: Path) -> None:
        # 创建超大文件（>1MB）
        big_file = tmp_path / "big.py"
        big_file.write_text("# big\n" + "x" * (1024 * 1024 + 100), encoding="utf-8")
        loader = PluginHotLoader(dropin_dir=tmp_path)
        entries = loader.scan_dropin_dir()
        assert entries == []

    def test_path_traversal_normal_file_allowed(self, tmp_path: Path) -> None:
        _write_dropin(tmp_path / "ok.py", "ok")
        loader = PluginHotLoader(dropin_dir=tmp_path)
        entries = loader.scan_dropin_dir()
        assert len(entries) == 1
        assert entries[0].name == "ok"


