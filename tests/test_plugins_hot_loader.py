# 5. reload_rollback
# ----------------------------------------------------------------------


class TestReloadRollback:
    """场景: reload 失败回滚（保留旧实例）。"""

    def test_reload_rollback_keeps_old_instance(self, tmp_path: Path) -> None:
        fpath = tmp_path / "p.py"
        _write_dropin(fpath, "v1")
        loader = PluginHotLoader(dropin_dir=tmp_path)
        loader.scan_dropin_dir()
        old_instance = loader.get_plugin("p")
        assert old_instance.name() == "v1"

        # 修改为损坏的插件
        time.sleep(0.05)
        _write_broken_dropin(fpath)
        os.utime(fpath, (time.time() + 5, time.time() + 5))

        # reload 应失败，但保留旧实例
        reloaded = loader.reload_if_changed()
        assert "p" not in reloaded  # 未成功重载
        # 旧实例仍可用
        assert loader.get_plugin("p") is old_instance
        assert loader.get_plugin("p").name() == "v1"

    def test_reload_rollback_logs_failure(self, tmp_path: Path) -> None:
        fpath = tmp_path / "p.py"
        _write_dropin(fpath, "v1")
        loader = PluginHotLoader(dropin_dir=tmp_path)
        loader.scan_dropin_dir()
        loader.clear_audit_log()

        time.sleep(0.05)
        _write_broken_dropin(fpath)
        os.utime(fpath, (time.time() + 5, time.time() + 5))

        loader.reload_if_changed()
        audits = loader.get_audit_log()
        reload_audits = [a for a in audits if a["method"] == "reload"]
        assert any(a["success"] is False for a in reload_audits)


