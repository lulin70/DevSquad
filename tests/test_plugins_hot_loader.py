class StubPlugin:
    """最小可识别的插件实例。"""

    def __init__(self, label: str = "stub") -> None:
        self.label = label

    def name(self) -> str:
        return self.label


# drop-in 插件文件模板（必须暴露 create_plugin()）
_DROPIN_TEMPLATE = '''
"""Drop-in plugin generated by test."""
from __future__ import annotations


class _Plugin:
    def __init__(self) -> None:
        self.tag = {tag!r}

    def name(self) -> str:
        return self.tag


def create_plugin() -> _Plugin:
    return _Plugin()
'''

_BROKEN_DROPIN_TEMPLATE = '''
"""Broken drop-in plugin (no create_plugin)."""
# 故意缺失 create_plugin() 函数
'''


def _write_dropin(filepath: Path, tag: Any) -> None:
    """写入一个合法的 drop-in 插件文件。"""
    filepath.write_text(_DROPIN_TEMPLATE.format(tag=tag), encoding="utf-8")


def _write_broken_dropin(filepath: Path) -> None:
    """写入一个损坏的 drop-in 插件文件（无 create_plugin）。"""
    filepath.write_text(_BROKEN_DROPIN_TEMPLATE, encoding="utf-8")


