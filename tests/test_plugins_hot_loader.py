class TestDropinDirScan:
    """场景: drop-in 目录扫描。"""

    def test_dropin_dir_scan_loads_new(self, tmp_path: Path) -> None:
        _write_dropin(tmp_path / "alpha.py", "alpha-tag")
        _write_dropin(tmp_path / "beta.py", "beta-tag")
        loader = PluginHotLoader(dropin_dir=tmp_path)

        entries = loader.scan_dropin_dir()
        assert len(entries) == 2
        names = {e.name for e in entries}
        assert names == {"alpha", "beta"}
        # 加载后可访问
        assert loader.get_plugin("alpha").name() == "alpha-tag"

    def test_dropin_dir_scan_empty_dir(self, tmp_path: Path) -> None:
        loader = PluginHotLoader(dropin_dir=tmp_path)
        assert loader.scan_dropin_dir() == []

    def test_dropin_dir_scan_skips_init_py(self, tmp_path: Path) -> None:
        _write_dropin(tmp_path / "real.py", "real")
        (tmp_path / "__init__.py").write_text("", encoding="utf-8")
        loader = PluginHotLoader(dropin_dir=tmp_path)
        entries = loader.scan_dropin_dir()
        assert len(entries) == 1
        assert entries[0].name == "real"

    def test_dropin_dir_scan_skips_already_registered(self, tmp_path: Path) -> None:
        _write_dropin(tmp_path / "dup.py", "dropin-tag")
        loader = PluginHotLoader(dropin_dir=tmp_path)
        loader.hot_register("dup", StubPlugin("hot"))
        # 已经 hot_register，scan 不应覆盖
        entries = loader.scan_dropin_dir()
        assert entries == []
        assert loader.get_plugin("dup").label == "hot"

    def test_dropin_dir_scan_broken_plugin_returns_none(self, tmp_path: Path) -> None:
        _write_broken_dropin(tmp_path / "broken.py")
        loader = PluginHotLoader(dropin_dir=tmp_path)
        entries = loader.scan_dropin_dir()
        assert entries == []
        # 损坏的插件不被注册
        assert loader.get_plugin("broken") is None

    def test_dropin_dir_scan_entry_source_is_dropin(self, tmp_path: Path) -> None:
        _write_dropin(tmp_path / "dp.py", "dp-tag")
        loader = PluginHotLoader(dropin_dir=tmp_path)
        loader.scan_dropin_dir()
        entry = loader.get_entry("dp")
        assert entry is not None
        assert entry.source == "dropin"
        assert entry.status == PluginStatus.LOADED
        assert entry.file_path != ""
        assert entry.checksum != ""

    def test_dropin_dir_scan_missing_dir_returns_empty(self, tmp_path: Path) -> None:
        loader = PluginHotLoader(dropin_dir=tmp_path / "does_not_exist")
        assert loader.scan_dropin_dir() == []


