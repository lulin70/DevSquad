class TestMtimePolling:
    """场景: mtime 轮询检测。"""

    def test_mtime_polling_no_change(self, tmp_path: Path) -> None:
        _write_dropin(tmp_path / "p.py", "v1")
        loader = PluginHotLoader(dropin_dir=tmp_path)
        loader.scan_dropin_dir()
        assert loader.reload_if_changed() == []

    def test_mtime_polling_detects_content_change(self, tmp_path: Path) -> None:
        # 注意：mtime 精度可能是秒，需要确保 mtime 变化或 checksum 变化
        fpath = tmp_path / "p.py"
        _write_dropin(fpath, "v1")
        loader = PluginHotLoader(dropin_dir=tmp_path)
        loader.scan_dropin_dir()
        assert loader.get_plugin("p").name() == "v1"

        # 修改内容
        time.sleep(0.05)
        _write_dropin(fpath, "v2")
        # 强制更新 mtime（某些 FS 精度低）
        os.utime(fpath, (time.time() + 5, time.time() + 5))

        reloaded = loader.reload_if_changed()
        assert "p" in reloaded
        assert loader.get_plugin("p").name() == "v2"

    def test_mtime_polling_ignores_builtin(self, tmp_path: Path) -> None:
        # builtin 不应被 reload_if_changed 触碰
        loader = PluginHotLoader(dropin_dir=tmp_path)
        loader.register_builtin("builtin", StubPlugin("v1"))
        assert loader.reload_if_changed() == []
        # builtin 仍在
        assert loader.get_plugin("builtin").label == "v1"


